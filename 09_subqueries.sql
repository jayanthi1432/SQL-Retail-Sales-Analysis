-- Orders above average amount
SELECT *
FROM ORDERS
WHERE TOTAL_AMOUNT >
(
SELECT AVG(TOTAL_AMOUNT)
FROM ORDERS
);

-- Highest Order
SELECT *
FROM ORDERS
WHERE TOTAL_AMOUNT =
(
SELECT MAX(TOTAL_AMOUNT)
FROM ORDERS
);

-- Lowest Order
SELECT *
FROM ORDERS
WHERE TOTAL_AMOUNT =
(
SELECT MIN(TOTAL_AMOUNT)
FROM ORDERS
);
